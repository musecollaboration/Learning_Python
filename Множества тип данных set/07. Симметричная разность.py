# ? .symmetric_difference()
# Позволяет выполнить операцию «разность множеств».
# Метод может принимать произвольное количество любых объектов, поддерживающих итерацию по своим элементам.
# Результатом вызова метода будет новое множество куда войдут только элементы из операции разности множеств.
# * Старые объекты никак не изменятся в процессе работы этого метода
set_a = {'car', 'soup', 'bus'}
set_b = {'bus', 'soup'}

res = set_a.symmetric_difference(set_b)
print(set_a)
print(set_b)
print(res)

print('-'*15)

set_num_1 = {1, 2, 3, 4}
