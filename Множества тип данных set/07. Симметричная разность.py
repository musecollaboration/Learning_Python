# ? .symmetric_difference_update()
# Метод позволяет выполнить операцию «симметрическая разность».
# * Метод может принимать только один любой объект, поддерживающий итерацию по своим элементам.
# * Результатом вызова метода будет не создание нового множества, а изменение существующего.
# * Присваивать ничего не нужно, автоматически после вызова изменится множество, у которого данный метод был вызван.
# * При попытке сохранить результат вызова этого метода  в переменную, в ней сохранится значение None
set_a = {'car', 'soup', 'bus'}
set_b = {'bus', 'soup', 'bro', 'lol'}

set_a.symmetric_difference_update(set_b)
print(set_a)                   # {'lol', 'bro', 'car'}
set_b.symmetric_difference_update(set_a)
print(set_b)                   # {'bus', 'soup', 'car'}

print('-'*15)

