# ? Симметричная разность множеств

# ? Симметрическая разность ( от английского symmetric difference) двух множеств – это операция,  позволяющая получить новое множество, в которое включены все элементы двух множеств, не принадлежащие одновременно обоим исходным множествам. Также симметрическую разность можно рассматривать как разность между объединением и пересечением исходных множеств. В математике симметрическая разность множеств A и В обозначается так A△B

# Результаты будут все элементы, за исключением тех которые принадлежат этим множествам (удалятся общие элементы)
a = {4, 3, 2, 1}
b = {3, 4, 5, 6, 7}
c = {10, 11, 12}
print(a ^ b)          # {1, 2, 5, 6, 7}

# Чтобы изменить множество, нужно выполнить
print(b)     # {3, 4, 5, 6, 7}
b ^= a
print(b)     # {1, 2, 5, 6, 7}

